let diaSemana = 8

switch(diaSemana){
    case 1:
        console.log("Dia lunes")
        break
    case 2:
        console.log("Dia Martes")
        break
    case 3:
        console.log("Dia Miercoles")
        break
    case 4:
        console.log("Dia Jueves")
        break
    case 5:
        console.log("Dia Viernes")
        break
    case 6:
        console.log("Dia Sabado")
        break
    case 7:
        console.log("Dia Domingo")
        break
    default:
        console.log(`El dia es erroneo (${diaSemana})`)

}