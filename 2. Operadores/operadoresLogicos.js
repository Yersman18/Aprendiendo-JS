// Operadores logicos
let a = true
let b = false

// es true si los dos son verdadero de lo contrario todos van hacer falsos
console.log(` ${a} && ${b} -> ${a && b} `)


// es falso si los dos son falsos de lo contrario siempre sera true
console.log(` ${a} || ${b} -> ${a || b} `)

// Operador ! not o NO
//invierte el valor original true -> false, false -> tru

console.log(` ${a} -> ${!a}`) // no necesita otro valor
